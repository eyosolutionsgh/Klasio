/**
 * One message, several channels.
 *
 * Announcements and bulk SMS were two unrelated features. Announcements were 49 lines that wrote a
 * row and fanned out to nobody; SMS had all the audience machinery and none of the portal. A head
 * wanting to tell families about Friday's closure wrote it twice, in two places, and the two said
 * slightly different things by the time they were sent.
 *
 * ## The two audiences were orthogonal all along
 *
 * SMS meant ALL | CLASS | LEVEL | CUSTOM — *which people*. An announcement meant
 * ALL | GUARDIANS | STUDENTS — *which portals*. Those answer different questions, so they are
 * separate fields here (`audienceScope`, `audienceRoles`) rather than one confused enum.
 *
 * Splitting them also makes a dead field live. `Announcement.audience` was read by the guardian
 * portal and the student portal but written by nobody, so every notice was "ALL" whatever the
 * school intended. It is now derived from `audienceRoles`, and those two readers become correct
 * for the first time.
 *
 * ## What a broadcast is, and is not
 *
 * It is not a delivery table. It EMITS: an Announcement for the portals, SmsMessage rows for
 * texts, BroadcastDelivery rows for email, SocialPost rows for social. One unified delivery table
 * would have swallowed the SMS credit ledger, the messaging screen's filters, and the four
 * automated senders that write SmsMessage directly with their own semantic batch ids — none of
 * which are broadcasts.
 */
